package chibuzo;

public class Ebuka {
    private String name;
    private int age;
    private double height;
    private String color;

    public Ebuka(String name, int age, double height, String color) {
        this.name = name;
        this.age = age;
        this.height = height;
        this.color = color;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }


    public void setColor(String color) {
        this.color = color;
    }

    public String getColor() {
        return color;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getHeight() {
        return height;
    }

    public int getAge() {
        return age;
    }



}




